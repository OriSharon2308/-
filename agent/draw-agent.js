/**
 * @param {{ messageText, history?, gender?, topic?, userId?, phase?, name?, geometry?:{width,height,grid}, occupied?:Array }} p
 * @returns {Promise<{reply:string, toolCalls:Array, mode:string}>}
 */
async function teacherDraw(p = {}) {
  const gender = p.gender === "female" ? "female" : "male";
  const phase = typeof p.phase === "string" && Object.prototype.hasOwnProperty.call(PHASE_DIRECTIVES, p.phase) ? p.phase : null;

  // ── פתיחת שיעור: תבנית מוכנה — בלי AI בכלל (לכן לפני בדיקת ה-AI). מיידי וחינם. ──
  if (phase === "opening") {
    const greeting = buildOpeningGreeting({ name: p.name, topic: p.topic, userId: p.userId });
    return { reply: genderize(greeting, gender), toolCalls: [], mode: "template", phase };
  }

  // מערך-השיעור של הנושא (לפי כמה שיעורים הילד כבר סגר בנושא) + מפתח-השיטה — כל שיעור במערך הוא שיטה נפרדת.
  let plan = null, methodKey = null;
  if (p.topic) {
    try {
      const tn = learnerProfile.topicLessonNumber(p.userId, p.topic);
      plan = course.planFor(p.topic, tn);
      methodKey = methods.keyFor(p.topic, tn);
    } catch (e) { /* מערך לא קריטי */ }
  }

  // ── שלב הוראה: שיטת-לימוד שמורה ומאושרת → מציגים ישר (בלי AI, בלי לחשוב מחדש). ──
  // רק בכניסה הראשונה לשלב (הילד לחץ "בוא נתחיל"); "לא הבנתי" תמיד מגיע ל-AI כדי ללמד אחרת.
  const isInstructEntry = phase === "instruct" && /בוא נתחיל/.test(String(p.messageText || ""));
  if (isInstructEntry && methodKey) {
    const m = methods.getConfirmed(methodKey);
    if (m) {
      methods.markUsed(methodKey);
      console.log(`[method] replay for "${methodKey}" (uses=${(m.uses || 0) + 1}) — 0 tokens`);
      return { reply: genderize(m.reply, gender), toolCalls: m.toolCalls, mode: "method", phase };
    }
  }

  if (!llm.isEnabled()) return { reply: "כדי לסרטט על הלוח צריך שירות AI מוגדר בשרת.", toolCalls: [], mode: "local" };

  let profileText = "";
  try {
    profileText = p.userId ? learnerProfile.toPromptText(p.userId) : "";
  } catch (e) {
    /* פרופיל לא קריטי */
  }

  // נוהל-שיעור: הנחיית-השלב בהודעת-המשתמש — לא ב-system — כדי לשמור על ה-cache.
  let phaseLine = phase ? `\n[שלב השיעור — ${phase}: ${PHASE_DIRECTIVES[phase]}]` : "";
  // מערך-השיעור: המורה מלמד את השיעור הספציפי ברצף (לא "משהו בנושא") — היסודות לפני החוקיות
  if (plan && (phase === "instruct" || phase === "guided" || phase === "independent")) {
    phaseLine +=
      `\n[מערך השיעור (${plan.index}/${plan.total} בנושא): "${plan.title}". המטרה: ${plan.goal}.` +
      (phase === "instruct" ? ` איך ללמד: ${plan.teach}` : " התרגילים חייבים לתרגל בדיוק את מטרת השיעור הזה — לא נושא כללי.") +
      (plan.review ? " (זו חזרה והעמקה — הילד כבר עבר את זה; העלה מעט את הרמה.)" : "") + "]";
  }
  // "לא הבנתי" בשלב ההוראה → המורה חושב מחדש ומלמד *אחרת* (לא חוזר על אותה שיטה)
  if (phase === "instruct" && !isInstructEntry) {
    phaseLine += "\n[הילד לא הבין את ההסבר הקודם — לַמד/י את אותו רעיון בדרך אחרת לגמרי: ייצוג שונה, דוגמה מהחיים, צעדים קטנים יותר. אל תחזור/י על אותו הסבר.]";
  }

  const geo = p.geometry || {};
  const W = Math.round(geo.width || 800), H = Math.round(geo.height || 560), G = Math.round(geo.grid || 42);
  const occN = Array.isArray(p.occupied) ? p.occupied.length : 0;
  const occLine = occN
    ? `הלוח כבר מכיל ${occN} ציורים — אם הבקשה אינה קשורה אליהם קרא/י קודם ל-clear_board, אחרת סרטט/י באזור פנוי.`
    : "הלוח ריק כרגע.";
  const ctxLine = `[הלוח כעת: ${W}×${H}, משבצת ${G}, מרכז בערך (${Math.round(W / 2)},${Math.round(H / 2)}). ${occLine}]`;

  // פריסת הלוח (id+מיקום+גודל) — כך המורה "רואה" מה כבר על הלוח ויכול לסדר בעצמו (move_item/resize_item/remove_item).
  const layout = Array.isArray(p.layout) ? p.layout : [];
  let layoutLine = "";
  if (layout.length) {
    const rows = layout
      .slice(0, 24)
      .map((it) => `  ${it.id} [${it.kind}] (${it.x},${it.y}) ${it.w}×${it.h}${it.label ? ` "${it.label}"` : ""}`)
      .join("\n");
    layoutLine =
      `\nפריטים שכבר על הלוח (פינה שמאלית-עליונה + גודל). אתה יכול לסדר אותם בעצמך לפי id — ` +
      `move_item(id,x,y) להזזה, resize_item(id,...) לשינוי גודל, remove_item(id) להסרה. ` +
      `אם משהו חופף או יושב לא טוב — סדר/י אותו, אל תוסיף/י עוד על הערימה:\n${rows}`;
  }

  const system = buildSystem({ gender, profileText, topic: p.topic || "", name: p.name ? String(p.name).slice(0, 40) : "" });
  const history = Array.isArray(p.history)
    ? p.history
        .filter((m) => m && (m.role === "user" || m.role === "assistant") && m.content)
        .slice(-12)
        .map((m) => ({ role: m.role, content: String(m.content) }))
    : [];
  const msgs = [...history, { role: "user", content: `${ctxLine}${layoutLine}${phaseLine}\n${String(p.messageText || "...")}` }];

  try {
    const t0 = Date.now();

    // סגירה = דיבור בלבד. אכיפה דטרמיניסטית: בלי כלים בכלל. גם בלי מצב-הלוח/פריסה — לא רלוונטיים לשיחה (חוסך טוקנים).
    if (phase === "closing") {
      const talkMsgs = [...history, { role: "user", content: `${phaseLine}\n${String(p.messageText || "...")}` }];
      const talk = await llm.complete({ system, messages: talkMsgs, maxTokens: 400, cacheSystem: true });
      console.log(`[timing] teach-closing: ${Date.now() - t0}ms`);
      let replyTxt = String(talk || "").replace(/\*\*/g, "").trim();
      if (!replyTxt) replyTxt = "כל הכבוד על שיעור מצוין! נתראה בפעם הבאה 👋";
      return { reply: genderize(replyTxt, gender), toolCalls: [], mode: "ai", phase };
    }

    const allCalls = [];
    let text = "";
    // לולאת Tool Use: מחזירים ל-Claude אישור לכל קריאה כדי שישלים ציורים מרובי-שלבים
    // (למשל שעון = עיגול + 12 מספרים + מחוגים). עוצרים כשסיים (end_turn) או בתקרה.
    for (let iter = 0; iter < 8; iter++) {
      const out = await llm.completeTools({ system, messages: msgs, tools: BOARD_TOOLS, maxTokens: 1200, cacheSystem: true });
      if (out.text) text = out.text;
      if (out.toolCalls.length) {
        for (const tc of out.toolCalls) allCalls.push(tc);
        msgs.push({ role: "assistant", content: out.content });
        msgs.push({
          role: "user",
          content: out.toolCalls.map((tc) => ({ type: "tool_result", tool_use_id: tc.id, content: "צויר על הלוח בהצלחה." })),
        });
      }
      if (out.stopReason !== "tool_use" || allCalls.length >= 60) break;
    }
    console.log(`[timing] teach-draw: ${Date.now() - t0}ms  tools=${allCalls.length}`);
    let reply = (text || "").replace(/\*\*/g, "");
    if (!reply) reply = allCalls.length ? "הנה, סרטטתי לך על הלוח." : "ספר/י לי מה לסרטט ואני אצייר על הלוח.";
    // שיטת-לימוד חדשה (כניסה ראשונה להוראה) → נשמרת כמועמדת; תאושר כשילד ילחץ "✓ הבנתי"
    if (isInstructEntry && methodKey && allCalls.length) {
      try { methods.save(methodKey, { reply, toolCalls: allCalls }); } catch (e) { /* שמירת-שיטה לא קריטית */ }
    }
    reply = genderize(reply, gender);
    return { reply, toolCalls: allCalls, mode: "ai" };
  } catch (e) {
    console.error("teach-draw error:", e.message);
    return { reply: "אופס, לא הצלחתי לסרטט כרגע. ננסה שוב?", toolCalls: [], mode: "error" };
  }
}

module.exports = { teacherDraw };
