// "שברים מתקדם" משתמש באותו מסלול שברים (התאמה מוכלת: "שברים מתקדם" ⊃ "שברים"),
// עם review=true אחרי הסיבוב הראשון — המורה מעלה רמה.

// אילו מפתחות-קורס מתאימים לנושא (התאמה מדויקת, ואז הכלה — "כפל וחילוק" יתפוס את "כפל")
function courseFor(topic) {
  const t = String(topic || "").trim();
  if (!t) return null;
  if (COURSES[t]) return COURSES[t];
  for (const key of Object.keys(COURSES)) {
    if (t.includes(key)) return COURSES[key];
  }
  return null;
}

/**
 * המערך לשיעור ה-n של הילד בנושא (n מתחיל מ-1).
 * אחרי סוף הרצף — חוזרים על השיעורים כחזרה-והעמקה (review:true → המורה מעלה רמה).
 */
function planFor(topic, n) {
  const plans = courseFor(topic);
  if (!plans || !plans.length) return null;
  const idx = Math.max(0, (Math.max(1, n) - 1)) % plans.length;
  const review = Math.max(1, n) > plans.length;
  return { ...plans[idx], index: idx + 1, total: plans.length, review };
}

module.exports = { COURSES, courseFor, planFor };
