function leafContent(key) {
  const c = CONTENT[key];
  if (c) return { key, method: c.method, substeps: c.substeps || [], tools: c.tools || [] };
  return { key, method: "תוכן ההוראה לנושא זה בהכנה.", substeps: [], tools: [] };
}

/** מחזיר את מסע-הלמידה של כיתה: מטרה + שלבים, כשכל שלב מכיל את העלים (תת-נושאים) עם תוכן ההוראה. */
function learnFlow(gradeNum) {
  const f = FLOW[gradeNum];
  if (!f) return null;
  return {
    grade: gradeNum,
    goal: f.goal,
    stages: f.stages.map((s) => ({
      key: s.key,
      label: s.label,
      leaves: (s.subs && s.subs.length ? s.subs : [s.key]).map(leafContent),
    })),
  };
}

